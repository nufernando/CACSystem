    public addEmployee(String primaryKey) throws ParseException{
        edit=1;
        initComponents();
        setcombobox();
        date.setDate(date1);
        group.add(male);
        group.add(female);
        primary_key=primaryKey;
        try {
            m.dbConnection();
            ResultSet rs = m.st.executeQuery("select * from employee where empNo ='"+primaryKey+"'");
            rs.next();
            epfNo.setText(rs.getString("epfNo"));
            name.setText(rs.getString(3));
            
            name1.setText(rs.getString(4));
            email.setText(rs.getString(6));
            phone.setText(rs.getString(7));
            address.setText(rs.getString(8));
            dob.setText(rs.getString(9));
            nic.setText(rs.getString(10));
            
            String u_level=rs.getString("user_level");
            switch(u_level){
                case "0": jComboBox1.setSelectedIndex(0); break;
                case "1": jComboBox1.setSelectedIndex(1); break;
                case "2": jComboBox1.setSelectedIndex(2); break;
                case "3": jComboBox1.setSelectedIndex(3); break;
                case "4": jComboBox1.setSelectedIndex(4); break;
                case "5": jComboBox1.setSelectedIndex(5); break;   
            }
            
            String gender = rs.getString("gender");
            if(gender.equals("Male")){
                male.setSelected(true);
            }
            else if(gender.equals("Female")){
                female.setSelected(true);
            }
            //m.loadImage(primaryKey, img);
            //date.setText(rs.getString(11));
            String s=rs.getString(11);
            DateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            Date ed=df.parse(s);
            date.setDate(ed);
        } catch (SQLException ex) {
            Logger.getLogger(addEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
        //epfNo.setText(primaryKey);
        m.getImage(img, primaryKey);
        epfNo.setEditable(false);
        jLabel1.setText("Edit Employee");
    }
